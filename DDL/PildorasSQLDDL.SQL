  CREATE DATABASE IF NOT EXISTS PildorasSQL;

	 USE PildorasSQL;

 CREATE TABLE IF NOT EXISTS CLIENTES(
   CODIGOCLIENTE VARCHAR(4) NOT NULL PRIMARY KEY
  ,EMPRESA       VARCHAR(24) NOT NULL
  ,DIRECCION     VARCHAR(19) NOT NULL
  ,POBLACION     VARCHAR(9) NOT NULL
  ,TELEFONO      VARCHAR(9) NOT NULL
  ,RESPONSABLE   VARCHAR(17) NOT NULL
  ,HISTORIAL     VARCHAR(30)
);

CREATE TABLE IF NOT EXISTS PRODUCTOS(
   CÓDIGOARTÍCULO VARCHAR(4) NOT NULL PRIMARY KEY
  ,SECCIÓN        VARCHAR(10) NOT NULL
  ,NOMBREARTÍCULO VARCHAR(19) NOT NULL
  ,PRECIO         VARCHAR(9) NOT NULL
  ,FECHA          DATE  NOT NULL
  ,IMPORTADO      VARCHAR(9) NOT NULL
  ,PAÍSDEORIGEN   VARCHAR(9) NOT NULL
  ,FOTO           VARCHAR(30)
);
